
public class substring {

	public static void main(String[] args) {
		String s="subashree";
	System.out.println(s.substring(4));//starting index is inclusive
		System.out.println(s.substring(4,7));
		String s1="I am the robot";
		System.out.println(s1.substring(5,10));//ending inde is exclusive
		
		

	}

}
